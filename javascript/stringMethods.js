// String interpolation
const name1 = 'tenzin';
const age = 60;
console.log(`My name is ${name1} and I am ${age} years old.`);

// startsWith() method
const string = 'Welcome to new world.';
console.log(string.startsWith('Wel')); 
console.log(string.startsWith('the'));

// endsWith() method
console.log(string.endsWith('world.')); 
console.log(string.endsWith('earth.')); 

// includes() method
console.log(string.includes('new')); 
console.log(string.includes('ddjfkd')); 

// repeat() method
const messg = 'Hello World ';
console.log(messg.repeat(3)); 

// trim() method
const str = '   Hello, World!   ';
console.log(str.trim()); 
